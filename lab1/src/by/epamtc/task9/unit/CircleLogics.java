package by.epamtc.task9.unit;

public class CircleLogics {
    public double calculateLengthOfCircle(double radius) {
        double pi = java.lang.Math.PI;
        return 2 * pi * radius;
    }

    public double calculateSquareOfCircle(double radius) {
        double pi = java.lang.Math.PI;
        return radius * radius * pi;
    }
}
