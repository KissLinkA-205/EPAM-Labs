package by.epamtc.task8.unit;

public class TrianleLogics {
    public static boolean comparisonWithThree(double x) {
        return x >= 3;
    }

    public static double functionGreaterThanTree(double x) {
        return -x * x + 3 * x + 9;
    }

    public static double functionLessThanTree(double x) {
        return (1 / (x * x * x - 6));
    }
}
